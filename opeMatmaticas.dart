void main() {
  int n1 = 10;
  int n2 = 5;

  //ADIÇÃO
  print('ADIÇÃO =  ${n1 + n2}');
  //SUBTRAÇÃO
  print('SUBTRAÇÃO = ${n1 - n2}');
  //DIVISÃO PARTE INTEIRA
  print('DIVISÃO PARTE INTEIRA = ${n1 ~/ n2}');
  //RESTO
  print('RESTO = ${n1 % n2}');
  //DIVISÃO
  print('DIVISÃO = ${n1 / n2}');
  //MULTIPLICAÇÃO
  print('MULTIPLICAÇÃO = ${n1 * n2}');
  //CALCULO
  print('CALCULO SIMPLES = ${n1 + (n2 * n2)}');

  //INCREMENTA MAIS 1 USANDO ++
  n2++;
  print('INCREMENTA +1 AO VALOR = $n2');
  //DECREMENTA
  n1--;
  print('DECREMENTA -1 AO VALOR = $n1');
}

